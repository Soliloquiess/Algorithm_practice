package bubble.test.ex15;

import javax.swing.ImageIcon;
import javax.swing.JLabel;

import lombok.Getter;
import lombok.Setter;

//class Player ->new 가능한 애들. 
@Getter
@Setter
public class Player extends JLabel implements Moveable{
	
	private BubbleFrame mContext;
	//위치상태
	private int x;
	private int y;
	
	//플레이어의 방향
	
	private PlayerWay playerWay;
	
	//움직임 상태
	private boolean left;
	private boolean right;
	private boolean up;
	private boolean down;
	
	//벽에 충돌한 상태
	private boolean leftWallCrash;
	private boolean rightWallCrash;
	
	//플레이어 속도 상태
	private final int SPEED = 4;

	private final int JUMPSPEED = 3;
	
//	public boolean isLeft() {
//		return left;
//	}
	
	private ImageIcon playerR, playerL;
	
	public Player(BubbleFrame mContext) {
		this.mContext = mContext;
		initObject();
		initSetting();
		initBackgroundPlayerService();
	}
	
	private void initObject() {
		playerR= new ImageIcon("image/playerR.png");
		playerL= new ImageIcon("image/playerL.png");
	}
	private void initSetting() {
		x = 80;
		y = 535;
		
		left = false;
		right = false;
		up = false;
		down = false;
		
		leftWallCrash = false;
		rightWallCrash = false;
		
		playerWay = PlayerWay.RIGHT;
		this.setIcon(playerR);
		this.setSize(50,50);
		setLocation(x,y);
	}
	
	
	private void initBackgroundPlayerService() {
		new Thread(new BackgroundPlayerService(this)).start();
	}
	
	@Override
	public void attack() {
		new Thread(()-> {
			Bubble bubble = new Bubble(mContext);
			mContext.add(bubble); //화면에 add해줘야 나온다.
			//mCOntext가 버블 프레임이고 화면에 보여주게 된다.
			if(playerWay == PlayerWay.LEFT) {
				bubble.left();
			}else
			{
				bubble.right();
			}	 
		}).start();
		
	}
	
	//이벤트 핸들러
	@Override
	public void left() {
//		System.out.println("left");

		playerWay = PlayerWay.LEFT;
		left = true;
		new Thread(()-> {
			while(left) {
				setIcon(playerL);
				x=x-SPEED;
				setLocation(x,y);
				try {
					Thread.sleep(10);//0.01초
				} catch (InterruptedException e) {
					e.printStackTrace();
				}	
			}
		}).start();	
		
	}
	@Override
	public void right() {

//		System.out.println("right");

		playerWay = PlayerWay.RIGHT;
		right = true;
		new Thread(()-> {
			while(right) {
				
				setIcon(playerR);
				x=x+SPEED;
				setLocation(x,y);
				try {
					Thread.sleep(10);//0.01초
				} catch (InterruptedException e) {
					e.printStackTrace();
				}	
			}
		}).start();
	}
	
	//left+up, right+up 동시에 하려면 스레드 만들지 않고는 불가능
	@Override
	public void up() {
		up = true;
		new Thread(()-> {
			for(int i = 0; i<130/JUMPSPEED; i++) {
				y=y-JUMPSPEED;
				setLocation(x,y);
				try {
					Thread.sleep(10);//0.01초
				} catch (InterruptedException e) {
					e.printStackTrace();
				}	
			}
			up = false;	//좌우 메서드는 vk_메서드에서 false줌
			down(); //바로 다운 메서드 실행
		}).start();
	} 
	@Override
	public void down() {
		
		System.out.println("down");
		down = true;
		new Thread(()-> {
//			for(int i = 0; i<130/JUMPSPEED; i++) {	//for문으로 주면 안됨 무조건 130만큼 떨어지기 때문 while로 줘야됨
			while(down) {
				y=y+JUMPSPEED;
				setLocation(x,y);
				try {
					Thread.sleep(10);//0.01초
				} catch (InterruptedException e) {
					e.printStackTrace();
				}	
			}
			down = false;
		}).start();
		
	}
}
